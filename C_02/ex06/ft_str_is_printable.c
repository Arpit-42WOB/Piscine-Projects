int	ft_str_is_printable(char *str)
{
	int	i;
	int	f;

	i = 0;
	f = 1;
	if (!str)
		return (f);
	while (str[i] != '\0')
	{
		if (!(str[i] >= 32 && str[i] < 127))
			f = 0;
		i++;
	}
	return (f);
}
