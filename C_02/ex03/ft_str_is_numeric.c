int	ft_str_is_numeric(char *str)
{
	int	i;
	int	f;

	i = 0;
	f = 1;
	if (!str)
		return (f);
	while (str[i] != '\0')
	{
		if (!(str[i] >= '0' && str[i] <= '9'))
			f = 0;
		i++;
	}
	return (f);
}
