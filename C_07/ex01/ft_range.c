#include<stdlib.h>

int	*ft_range(int min, int max)
{
	int	*range;
	int	d;
	int	i;

	if (min >= max)
		return (NULL);
	d = max - min;
	range = (int *)malloc(sizeof(int) * d);
	if (!range)
		return (NULL);
	i = 0;
	while (min < max)
	{
		range[i] = min;
		i++;
		min++;
	}
	return (range);
}
