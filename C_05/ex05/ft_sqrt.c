int	ft_sqrt(int nb)
{
	long	s;
	long	m;
	long	e;

	s = 0;
	e = nb;
	if (nb < 1)
		return (0);
	while (s <= e)
	{
		m = (s + e) / 2;
		if ((m * m) == nb)
			return (m);
		if ((m * m) < nb)
			s = m + 1;
		else
			e = m -1;
	}
	return (0);
}
