int	ft_is_prime(int nb)
{
	int	i;
	int	f;

	i = 2;
	f = 0;
	if (nb < 2)
		return (0);
	while (i <= (nb / 2))
	{
		if ((nb % i) == 0)
			f = 1;
		i++;
	}
	if (f == 0)
		return (1);
	else
		return (0);
}
